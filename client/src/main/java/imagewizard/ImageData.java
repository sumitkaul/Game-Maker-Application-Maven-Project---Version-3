package imagewizard;

import java.io.File;

public class ImageData {
	private File file = null;
	private String tags = null;

	public ImageData() {

	}

	public File getFile() {
		return file;
	}

	public void setFile(File file) {
		this.file = file;
	}

	public String getTags() {
		return tags;
	}

	public void setTags(String tags) {
		this.tags = tags;
	}
	
	

}
